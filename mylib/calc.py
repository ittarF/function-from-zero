"""
A calculator module

"""


def add(a, b):
    """
    Add two numbers together

    """
    return a + b


def sub(a, b):
    """
    Subtract two numbers

    """
    return a - b


def mul(a, b):
    """
    Multiply two numbers

    """
    return a * b


def div(a, b):
    """
    Divide two numbers

    """
    return a / b


def power(a, b):
    """
    Raise a to the power of b

    """
    return a**b
